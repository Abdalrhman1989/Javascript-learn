// 1. Addition
let sum = 10 + 5;
console.log("Sum:", sum);  // Output: 15

// 2. Subtraction
let difference = 20 - 8;
console.log("Difference:", difference);  // Output: 12

// 3. Multiplication
let product = 6 * 7;
console.log("Product:", product);  // Output: 42

// 4. Division
let quotient = 50 / 10;
console.log("Quotient:", quotient);  // Output: 5

// 5. Addition with compound assignment
let total = 15;
total += 5;
console.log("Total after addition:", total);  // Output: 20

// 6. Subtraction with compound assignment
let balance = 100;
balance -= 25;
console.log("Balance after subtraction:", balance);  // Output: 75

// 7. Multiplication with compound assignment
let amount = 10;
amount *= 3;
console.log("Amount after multiplication:", amount);  // Output: 30

// 8. Division with compound assignment
let quantity = 100;
quantity /= 5;
console.log("Quantity after division:", quantity);  // Output: 20

// 9. Arithmetic expression
let result = (10 + 5) * 2;
console.log("Result of expression:", result);  // Output: 30

// 10. Compound assignment with an arithmetic expression
let x = 3;
x = x * 4 + 2;
console.log("Value of x:", x);  // Output: 14

// 11. Mixed expression
let mixedResult = (20 / 4) + (3 * 2);
console.log("Mixed Result:", mixedResult);  // Output: 11

// 12. Mixed expression with compound assignment
let y = 15;
y = (y - 7) / 2;
console.log("Value of y after operations:", y);  // Output: 4

// 13. Greater than
let isGreater = 10 > 5;
console.log("Is 10 greater than 5?:", isGreater);  // Output: true

// 14. Less than or equal to
let isLessThanOrEqual = 7 <= 7;
console.log("Is 7 less than or equal to 7?:", isLessThanOrEqual);  // Output: true

// 15. Equal to
let isEqual = 3 === 3;
console.log("Is 3 equal to 3?:", isEqual);  // Output: true