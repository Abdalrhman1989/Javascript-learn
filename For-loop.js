// 1. Print numbers from 1 to 5 using a for-loop.
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

// 2. Print all even numbers from 2 to 10 using a for-loop.
for (let i = 2; i <= 10; i += 2) {
    console.log(i);
}

// 3. Print the first 5 letters of the alphabet using a for-loop.
const letters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
for (let i = 0; i < 5; i++) {
    console.log(letters[i]);
}

// 4. Print numbers from 10 to 1 in descending order using a for-loop.
for (let i = 10; i >= 1; i--) {
    console.log(i);
}
